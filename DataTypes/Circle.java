// WAP to calculate area and circumference of circle.

class Circle{
	public static void main(String args[]){
		float r,ar,cr;
		r = 2.5f;
		// Initialization
		ar = 3.14f*r*r;
		cr = 2*3.14f*r;
		// Operation
		System.out.println("Area of Circle: "+ar);
		System.out.println("Circumference of Circle: "+cr);
	}
}
		